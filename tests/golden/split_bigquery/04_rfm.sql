s0 AS (
  SELECT *,
    -- cut-off date is 29th march, 2026
  -- last day of FY25
  ABS(DATE_DIFF(DATE("2026-03-29"), `LATEST_ORDER_DATE`, DAY)) AS `RECENCY`
  FROM `filters`
),
