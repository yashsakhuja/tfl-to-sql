  END AS `FREQUENCY_208W`
  FROM s3
),
s5 AS (
  SELECT *,
    CASE
  WHEN `DATE` >= `START_OF_ANALYSIS_PERIOD_260W` AND `DATE` <= `END_OF_ANALYSIS_PERIOD` THEN `ORDER_ID`
  END AS `FREQUENCY_260W`
  FROM s4
