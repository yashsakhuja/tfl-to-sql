  SELECT *,
    CASE
  WHEN `DATE` >= `START_OF_ANALYSIS_PERIOD_52W` AND `DATE` <= `END_OF_ANALYSIS_PERIOD` THEN `ORDER_ID`
  END AS `FREQUENCY_52W`
  FROM s0
),
